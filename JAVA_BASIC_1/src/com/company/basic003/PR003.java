package com.company.basic003;

public class PR003 {
	public static void main(String[] args) {
		//Q1
		for (int i=1;i<=3;i++) {
			System.out.printf("%d\t",i);
		}
		
		//Q2
		int j=1;
		while (j<=3) {
			System.out.printf("%d\t",j);
			j++;
		}
		
		//Q3
		int i=1;
		do {
			System.out.printf("%d\t",i);
			i++;
		} while(i<=3);
	}
}
