package com.company.basic001;

public class A002_basic {
	public static void main(String[] args) {
		// System.out.print()		줄바꿈 x
		System.out.print("줄바꿈 X");
		
		// System.out.println()		줄바꿈 O
		System.out.println("줄바꿈 O");
		System.out.println("-");
		
		// System.out.printf() 		출력형식 갯수 - %d(정수), %f(실수), %s(문자열)
		System.out.printf("%d, %s, %f", 1,"Hello", 1.23);
	}
}
