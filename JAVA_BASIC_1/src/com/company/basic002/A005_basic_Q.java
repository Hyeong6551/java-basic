package com.company.basic002;

public class A005_basic_Q {
	public static void main(String[] args) {
		//Q1
		for(int i=1;i<=5;i++) {
			System.out.print(i);
		}
		System.out.println("\n");
		
		//Q2
		for(int i=5;i>=1;i--) {
			System.out.print(i);
		}
		System.out.println("\n");
		
		//Q3
		for(int i=1;i<=3;i++) {
			System.out.print("JAVA"+i);
		}
		System.out.println("\n");
		
		//Q4
		for(int i=3;i>=1;i--) {
			System.out.print("HAPPY"+i);
		}
		System.out.println("\n");
		
		//Q5
		for(int i=0;i<=2;i++) {
			System.out.print(i+" ");
		}
		System.out.println("\n");
		
		//Q6
		for(int i=0;i<=99;i++) {
			System.out.print(i+" ");
		}
		System.out.println("\n");
		
		//Q7
		for(int i=10;i>=1;i--) {
			System.out.print(i+" ");
		}
		System.out.println("\n");
		
		//Q8
		for(int i=0;i<=8;i=i+2) {
			System.out.print(i+" ");
		}
		System.out.println("\n");
		
		//Q9
		for(int i=0,j=0;i<10;i++,j++) {
			System.out.print(i+j+" ");
		}
	}
}
