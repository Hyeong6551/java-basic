package com.company.basic001;

public class A003_basic_Q {
	public static void main(String[] args) {
		// Q1
		int apple;
		apple = 1000;
		System.out.println("사과가격은 "+apple+"입니다");
		apple = 2000;
		System.out.println("사과가격은 "+apple+"입니다");
		
		// Q2
		int a = 10;
		int b = 3;
		
		System.out.println(a+"+"+b+"="+(a+b));
		System.out.println(a+"-"+b+"="+(a-b));
		System.out.println(a+"*"+b+"="+(a*b));
		System.out.println(a+"/"+b+"="+(a/(float)b));
	}
}
